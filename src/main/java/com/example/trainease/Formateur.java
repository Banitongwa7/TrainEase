package com.example.trainease;

public class Formateur {
    private int id;
    private String nom;
    private String prenom;
    private String email;
    private String phone;

    public Formateur() {

    }

    public Formateur(int id, String nom, String prenom, String email, String phone) {
        this.id = id;
        this.nom = nom;
        this.prenom = prenom;
        this.email = email;
        this.phone = phone;
    }




}
